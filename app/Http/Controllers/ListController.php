    {
        $request->validate([
            'list_name' => 'required|string|min:3|max:60|unique:lists'
        ]);

        $list=new Roll;

        $list->user_id = auth()->user()->id;
        $list->list_name = $request->list_name;

        $list->save();

        return back()->with('success','List added successfully');

